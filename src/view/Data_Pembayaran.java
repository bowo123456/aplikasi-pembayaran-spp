    private void tblPembayaranMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPembayaranMouseClicked
          JTable tbl = (JTable)evt.getSource();
        int baris = tbl.getSelectedRow();
        txtKdKelas.setText(tbl.getModel().getValueAt(baris, 5).toString());
        txtNis.setText(tbl.getModel().getValueAt(baris, 2).toString());
        txtNama.setText(tbl.getModel().getValueAt(baris, 4).toString());
        txtKelas.setText(tbl.getModel().getValueAt(baris, 6).toString());
            String dateValue = String.valueOf(tbl.getModel().getValueAt(tbl.getSelectedRow(),7));
      java.util.Date date = null;
        try {
           // date = new SimpleDateFormat("dd MMMMM yyyy").parse(dateValue);
              date = new SimpleDateFormat("dd MMMMM yyyy", new Locale("id")).parse(dateValue);
        } catch (ParseException ex) {
            System.out.println(ex);
        }
        tglPembayaran.setDate(date);
        
    cbJenisPembayaran.setSelectedItem(tbl.getModel().getValueAt(baris, 8).toString());
            txtKeterangan.setText(tbl.getModel().getValueAt(baris, 9).toString());
        
            setEditButton(true);
    }//GEN-LAST:event_tblPembayaranMouseClicked

