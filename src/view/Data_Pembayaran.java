    void tambah(){
        cp.setNis(Integer.parseInt(txtNis.getText()));
        cp.setId_kelas(Integer.parseInt(txtKdKelas.getText()));
           SimpleDateFormat sdf = new SimpleDateFormat("dd MMMMM yyyy", new Locale("id"));
        String tgl_lhir_siswa = String.valueOf(sdf.format(tglPembayaran.getDate()));
        cp.setTgl_pembayaran(tgl_lhir_siswa);
        cp.setJenis_pembayaran(cbJenisPembayaran.getSelectedItem().toString());
        cp.setKeterangan(txtKeterangan.getText());
        cp.tambah_data();
        tampil();
        bersih();
    }
